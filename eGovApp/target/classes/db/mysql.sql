CREATE TABLE MEMBER (USER_ID VARCHAR(30) NOT NULL PRIMARY KEY, NAME VARCHAR(255) NOT NULL, PASSWORD VARCHAR(255) NOT NULL, BIRTH VARCHAR(255) NOT NULL, EMAIL VARCHAR(255) NOT NULL, PHONE VARCHAR(255), USER_SE VARCHAR(30) NOT NULL);
CREATE TABLE BOARD (board_id INT AUTO_INCREMENT PRIMARY KEY, author VARCHAR(50) NOT NULL, title VARCHAR(200) NOT NULL, content TEXT NOT NULL, created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP, allow_comment BOOLEAN NOT NULL DEFAULT TRUE, comment_cnt INT NOT NULL DEFAULT 0, views INT NOT NULL DEFAULT 0, FOREIGN KEY (author) REFERENCES MEMBER(USER_ID));
CREATE TABLE BOARD_FILE (file_id INT AUTO_INCREMENT PRIMARY KEY, post_id INT NOT NULL, file_seq INT NOT NULL, file_name VARCHAR(500), file_path VARCHAR(500) NOT NULL, file_size BIGINT NOT NULL, FOREIGN KEY (post_id) REFERENCES BOARD(board_id) ON DELETE CASCADE,
INDEX idx_post_seq (post_id, file_seq));


